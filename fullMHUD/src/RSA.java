    private void randomKey() throws NoSuchAlgorithmException, InvalidKeySpecException 
    {
        SecureRandom sr = new SecureRandom();
        KeyPairGenerator kpg = KeyPairGenerator.getInstance("RSA");
        kpg.initialize(1024, sr);
        KeyPair kp = kpg.genKeyPair();
        PublicKey publicKey = kp.getPublic();
        PrivateKey privateKey = kp.getPrivate();
        txtkeypublic.setText(Base64.getEncoder().encodeToString(publicKey.getEncoded())) ;
        txtprivatekey.setText(Base64.getEncoder().encodeToString(privateKey.getEncoded()));
        

    }

