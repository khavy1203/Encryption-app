    private void btnencrypActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnencrypActionPerformed
        
        try {
            if(txtkeypublic.getText().equals("")|| txtprivatekey.getText().equals(""))
            {
                JOptionPane.showMessageDialog(rootPane,"CHƯA TẠO KEY \n NHẤN RANDOM ĐỂ TẠO KEY");
                return;
            }
            if(txtinput.getText().equals(""))
            {
                JOptionPane.showMessageDialog(rootPane,"CHƯA NHẬP NỘI DUNG ĐỂ MÃ HÓA ");
                return;
            }
            // TODO add your handling code here:
            //tạo public key
            X509EncodedKeySpec spec = new X509EncodedKeySpec(Base64.getDecoder().decode(txtkeypublic.getText()));
            KeyFactory factory = KeyFactory.getInstance("RSA");
            try {
                PublicKey pubKey = factory.generatePublic(spec);
                Cipher c = Cipher.getInstance("RSA");
                c.init(Cipher.ENCRYPT_MODE, pubKey);
                String msg =txtinput.getText();
                byte encryptOut[] = c.doFinal(msg.getBytes());
                String strEncrypt = Base64.getEncoder().encodeToString(encryptOut);
                txtcipher.setText(strEncrypt);
            } catch ( NoSuchPaddingException | InvalidKeyException | IllegalBlockSizeException | BadPaddingException ex) {
              JOptionPane.showMessageDialog(rootPane,"VĂN BẢN MÃ HÓA KHÔNG HỢP LỆ \n ĐỘ DÀI QUÁ LỚN");
            }
            catch (InvalidKeySpecException ex )
            {
                JOptionPane.showMessageDialog(rootPane,"KEY KHÔNG HỢP LỆ");
            }
        } catch (NoSuchAlgorithmException ex) {
            JOptionPane.showMessageDialog(rootPane,"VĂN BẢN MÃ HÓA KHÔNG HỢP LỆ -- ");
        }
        catch (IllegalArgumentException ex){
            JOptionPane.showMessageDialog(rootPane,"KEY KHÔNG HỢP LỆ");
        }
        
    }//GEN-LAST:event_btnencrypActionPerformed

    private void btndecrypActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndecrypActionPerformed
        try {
            // TODO add your handling code here:
            PKCS8EncodedKeySpec spec = new PKCS8EncodedKeySpec(Base64.getDecoder().decode(txtprivatekey.getText()));
            KeyFactory factory = KeyFactory.getInstance("RSA");
            PrivateKey priKey = factory.generatePrivate(spec);
            // giải mã
            Cipher c = Cipher.getInstance("RSA");
            c.init(Cipher.DECRYPT_MODE, priKey);
            byte decryptOut[] = c.doFinal(Base64.getDecoder().decode(txtcipher.getText()));
            txtplain.setText(new String(decryptOut));
        } catch (NoSuchAlgorithmException | InvalidKeySpecException | NoSuchPaddingException | InvalidKeyException | IllegalBlockSizeException | BadPaddingException ex) {
            JOptionPane.showMessageDialog(rootPane,"KEY KHÔNG HỢP LỆ");
        }
        catch(IllegalArgumentException  ex){
            JOptionPane.showMessageDialog(rootPane,"MẬT MÃ KHÔNG HỢP LỆ");
        }
    }//GEN-LAST:event_btndecrypActionPerformed

